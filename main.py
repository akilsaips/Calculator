def add(n1,n2):
  return n1+n2

def subtract(n1,n2):
  return n1-n2

def multiply(n1,n2):
  return n1*n2

def divide(n1,n2):
  return n1/n2

operation = {
  "+": add,
  "-": subtract,
  "*": multiply,
  "/": divide
}


def calculator():
    num1 = float(input("Enter the first number? "))

    for symbol in operation:
        print(symbol)

    shouldcontinue = True

    while shouldcontinue:
        pick_operation = input("Pick an operation: ")

        num2 = float(input("Enter the next number: "))

        chose_operation = operation[pick_operation]

        answer = chose_operation(num1,num2)

        print(f"{num1}{pick_operation}{num2} = {answer}")

        resume = input("Enter y to contine, n to start a fresh calculation: or press exit to quit. ").lower()

        if resume == "y":
            num1 = answer
        elif resume == "n":
            shouldcontinue = False
            calculator()
        else:
          print("Thanks for calculating!")
          break


calculator()
